a = "What's this?"
print("%-10.4s" % a)
print("%10.4s" % a)

'''
정답:
What      
      What
      
- `%10s` : 10칸 공간에 오른쪽 정렬
- `%-10s` : 10칸 공간에 왼쪽 정렬
- `%.4s` : 앞에서 4글자만 잘라서 출력
- `%10.4s` : 10칸 공간, 4글자만
- `%s`에서 왼쪽 정렬/오른쪽 정렬은 `-`유무로 결정

- 첫 줄: `'What'` + 공백 6칸 (왼쪽 정렬)
- 둘째 줄: 공백 6칸 + `'What'` (오른쪽 정렬)
'''