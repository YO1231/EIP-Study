#include <stdio.h>
void main()
{
    float a = 7.9;

    printf("%f\n", a);
    printf("%.3f\n", a);
    printf("%6.2f\n", a);
}

/*
정답:
7.900000
7.900
  7.90

- 첫 번째 printf("%f\n", a): %f는 실수를 기본 6자리 소수점으로 출력 → 7.900000
- 두 번째 printf("%.3f\n", a): %.3f는 소수점 아래 3자리까지만 출력 → 7.900
- 세 번째 printf("%6.2f\n", a): %6.2f는 전체 6자리 폭 확보, 소수점 아래 2자리 출력 → " 7.90" (앞에 공백 2개)
*/